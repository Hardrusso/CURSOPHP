<?php

// Operadores Aritmeticos

$numero1 = 55;
$numero2 = 33;

echo 'Suma: '.($numero1+$numero2).'<hr/>';
echo 'Resta: '.($numero1-$numero2).'<hr/>';
echo 'Multiplicación: '.($numero1*$numero2).'<hr/>';
echo 'División: '.($numero1/$numero2).'<hr/>';
echo 'Resto: '.($numero1%$numero2).'<hr/>';


// Operadores de Incremnto y Decremento

$year = 2019;

// Incremento
// $year = $year + 1;
$year++;

echo '<h1>'.$year.'</h1>';

// Decremento
// $year = $year + 1;
$year--;

echo '<h1>'.$year.'</h1>';

// Pre-Incremento
// $year = 1+ $year ;
++$year;

echo '<h1>'.$year.'</h1>';

// Pre-Decremento
// $year = 1- $year;
--$year;

echo '<h1>'.$year.'</h1>';

